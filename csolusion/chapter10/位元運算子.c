#include<stdio.h>
int main()
{
    printf("21&9=%d\n",21&9);//10101&01001=00001=1
    printf("21|9=%d\n",21|9);//10101|01001=11101=29
    printf("21^9=%d\n",21^9);//10101^01001=11100=28
    printf("21>>2=%d\n",21>>2);//10101>>2=00101=5==/2的n次方
    printf("21<<2=%d\n",21<<2);//10101<<2=1010100=84(左位元不會被吃掉)==乘以2的n次方
    printf("~21=%d\n",~21);//~10101=01010=-(32-10)
    return 0;
}